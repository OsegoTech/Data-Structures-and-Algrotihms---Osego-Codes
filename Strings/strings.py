# append to end of a string - O(n) time complexity
s= "Hello, World!"
b = s + " Welcome to Python"
print(b)

# check if a substring is in a string - O(n) time complexity
print("Hello" in s)


# access different positions in a string - O(1) time complexity
print(s[0])

# length of a string - O(1) time complexity
print(len(s))